type RuntimeTranslationDictionary = {
  exactTextMap: Record<string, string>;
  regexTranslations: RegexTranslation[];
  fragments: Array<[string, string]>;
};

const RUNTIME_TRANSLATIONS: Partial<Record<Locale, RuntimeTranslationDictionary>> = {
  en: {
    exactTextMap,
    regexTranslations,
    fragments,
  },
};

export function getIntlLocale(locale: Locale): string {
  return getIntlLocaleTag(locale);
}

export function t(locale: Locale, input: string): string {
  if (locale === DEFAULT_LOCALE) return input;

  const dictionary = RUNTIME_TRANSLATIONS[locale];
  if (!dictionary) return input;

  const trimmed = input.trim();
  if (!trimmed) return input;

  if (dictionary.exactTextMap[trimmed]) {
    return input.replace(trimmed, dictionary.exactTextMap[trimmed]);
  }

  for (const rule of dictionary.regexTranslations) {
    const match = trimmed.match(rule.pattern);
    if (match) return input.replace(trimmed, rule.replace(...match));
  }

  let next = input;
  for (const [from, to] of dictionary.fragments) {
    next = next.replaceAll(from, to);
  }
  return next;
}
